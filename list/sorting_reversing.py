#Sorting List

cars=['toyota','honda','audi','bmw']
print(cars)
cars.sort()
print(cars)
cars.sort(reverse=True)
print(cars)
print("----------------------------------------------")


#Sorted | Doesn't affect original list

cars2=['toyota','honda','audi','bmw']
print(sorted(cars2))
print(cars2)
print("----------------------------------------------")


#Reversing List

numbers2=[1,2,3,4]
numbers2.reverse()
print(numbers2)
print("----------------------------------------------")


